            <h2>Le Sando</h2>
            <p>
                Le Katsu Sando, ou Sando pour les intimes, est aux japonais ce que le jambon-beurre est aux français. Ce
                sandwich a vu le jour avec le déve- loppement des chemins de fer. Facile à manger, nourrissant, il est
                rapi- dement devenu culte. Pour Walter Ishizuka c’est un souvenir d’enfance.Son père le préparait pour
                les pique-niques familiaux. Lui-même cuisinier d’origine japonaise, installé en France depuis les années
                1970, il préparait une version personnelle, différente de la recette traditionnelle au porc pané.
            </p>
            <p>
                Aujourd’hui, c’est au tour du fils de proposer sa vision du Sando. Mais que les puristes se rassurent,
                les marqueurs sont bel et bien respectés. Pain ultra moelleux, panure panko croustillante, sauce
                tonkatsu, tout y est, avec une touche de mieux.
            </p>
            <p>
                Le fameux Shokupan, pain spécifique au Sando est réalisé par JULHÈS, à partir de 2 recettes (un pain
                blanc et un pain noir au charbon) spéciale- ment mises au point avec le chef, qui explique : « J’adore
                le Sando, mais ça peut être casse-gueule à manger. C’est pour ça que j’ai voulu un pain plus brioché
                qu’à l’ordinaire. On le toaste légèrement pour en faire ressortir le goût.
            </p>
            <p>
                C’est hyper fondant et beaucoup plus facile à déguster. » Côté garnitures, un veggie et trois versions
                sont proposées avec des pro- duits de première qualité: un bœuf wagyu maturé à point, un saumon d’Écosse
                qui reste bien moelleux après cuisson et un bon poulet fermier. Le tout pané dans les règles de l’art
                avec de l’œuf et une panure panko, moelleuse et croustillante. Enfin, pour la gourmandise, la fraîcheur
                et le croquant, chaque version est assortie d’un coleslaw japonais et de pickles maison, mais aussi
                d’une sauce particulière qui se marie avec la sauce tonkatsu.

            </p>
            <p>Pour les desserts, le chef innove aussi, avec un sandwich glacé et un sand- wich sucré, parfum sésame
                noir ou chocolat noisette. Les thés et les cafés sont confiés à la Maison Coutume, et les limonades bio
                à la marque Lemo- naid & ChariTea. </p>
